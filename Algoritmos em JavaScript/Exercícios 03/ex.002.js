console.log('FOR:')
for (var cont = 0; cont <= 100; cont++) {
    console.log(cont)
}
console.log()
console.log('WHILE:')
var cont = 0
while (cont < 100) {
    cont++
    console.log(cont)
}
console.log()
console.log('DO WHILE:')
var cont = 0
do {
    cont++
    console.log(cont)
} while (cont < 100)